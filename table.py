def run_solvers(n, W, u, w, label):
    results = []

    def evaluate(sol): return sum(u[i] for i in range(n) if sol[i])
    def get_weight(sol): return sum(w[i] for i in range(n) if sol[i])

    # 1. Greedy Ratio
    start = time.time()
    items = sorted(range(n), key=lambda i: (u[i]/w[i], w[i]), reverse=True)
    sol, cur_w = [0]*n, 0
    for i in items:
        if cur_w + w[i] <= W: sol[i] = 1; cur_w += w[i]
    results.append(("Greedy Ratio", evaluate(sol), time.time() - start))

    # 2. Greedy Max Utility
    start = time.time()
    items = sorted(range(n), key=lambda i: u[i], reverse=True)
    sol, cur_w = [0]*n, 0
    for i in items:
        if cur_w + w[i] <= W: sol[i] = 1; cur_w += w[i]
    results.append(("Greedy Utility", evaluate(sol), time.time() - start))

    # 3. Greedy Min Weight
    start = time.time()
    items = sorted(range(n), key=lambda i: w[i])
    sol, cur_w = [0]*n, 0
    for i in items:
        if cur_w + w[i] <= W: sol[i] = 1; cur_w += w[i]
    results.append(("Greedy Weight", evaluate(sol), time.time() - start))

    # 4. VND
    start = time.time()
    # Initial sol from ratio
    items = sorted(range(n), key=lambda i: (u[i]/w[i], w[i]), reverse=True)
    curr_sol, cur_w = [0]*n, 0
    for i in items:
        if cur_w + w[i] <= W: curr_sol[i] = 1; cur_w += w[i]
    
    # 1-Flip Improvement
    improved = True
    while improved:
        improved = False
        for i in range(n):
            new_w = cur_w - w[i] if curr_sol[i] == 1 else cur_w + w[i]
            if new_w <= W:
                new_v = evaluate(curr_sol) - u[i] if curr_sol[i] == 1 else evaluate(curr_sol) + u[i]
                if new_v > evaluate(curr_sol):
                    curr_sol[i] = 1 - curr_sol[i]; cur_w = new_w; improved = True; break
    results.append(("VND", evaluate(curr_sol), time.time() - start))

    # 5. VNS (Simplified for 1 sec run)
    start = time.time()
    best_sol = list(curr_sol)
    while time.time() - start < 1.5:
        # Shaking
        x_p = list(best_sol)
        idx = random.randint(0, n-1)
        x_p[idx] = 1 - x_p[idx]
        if get_weight(x_p) <= W and evaluate(x_p) > evaluate(best_sol):
            best_sol = list(x_p)
    results.append(("VNS", evaluate(best_sol), time.time() - start))

    # Sort results by Z value descending
    results.sort(key=lambda x: x[1], reverse=True)
    
    print(f"\n{'='*15} RESULTS FOR {label} {'='*15}")
    print(f"{'Method':<20} | {'Utility (Z)':<12} | {'Time (s)':<10}")
    print("-" * 50)
    for res in results:
        print(f"{res[0]:<20} | {res[1]:<12} | {res[2]:.6f}")

